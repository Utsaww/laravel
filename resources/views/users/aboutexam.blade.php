<h2>SELECTION PROCEDURE</h2>
<h4>( TWO-STAGE SELECTION PROCESS )</h4>

<p>Identification of talent comprises two-stage selection process. While the individual State/UT conducts the first stage selection, the second stage selection at the national level is carried out by the NCERT.

<br><br><b>Stage I  (STATE LEVEL EXAMINATION)</b><br><br>

Each State/UT conducts its own examination. They have the autonomy to lay down their own norms for the purpose of determining the eligibility of the candidates. The examination consist of Mental Ability Test (MAT) and Scholastic Ability Test (SAT). Students who qualify Stage - I are eligible to appear for NTSE Stage – II to be conducted by the NCERT. This number is based on the enrolment of students at Classes IX and X in different States/UTs. The candidates are to be recommended on the basis of the merit in the written examination conducted by the States/UTs. The details of the number of candidates for different States/UTs as applicable are notified separately. The state level screening examination is conducted in all State/UT’s in the month of November. 

<br><br><b>Eligibility</b><br><br>
All students studying in Class X in any type of recognized school including Kendriya Vidyalaya, Navodaya Vidyalaya, and Sainik School etc. will be eligible to appear at the State Level Examination from the State in which the school is located. However, no domicile restriction shall be imposed. The state may impose any other eligibility condition for appearing in the screening examination like any qualifying percentage of marks in the previous annual examination etc. Students registered under Open and Distance Learning (ODL) are also eligible for scholarship provided the students are below the age of 18 years (as on 1st July of that particular year) the students is not employed and they are appearing in class X for the first time.

<br><br><b>Stage II  (NATIONAL LEVEL EXAMINATION)</b><br><br>

This Examination is conducted by NCERT. The examination consist of Mental Ability Test (MAT) and Scholastic Ability Test (SAT) - same as Stage - I.  the autonomy to lay down their own norms for the purpose of determining the eligibility of the candidates. 

<br><br><b>Eligibility</b><br><br>
Students who qualify Stage - I are eligible to appear for NTSE Stage – II. The National Talent Search Examination is open for students of Indian nationality whether they study in India or abroad at Class X level. Only the candidates selected by the States/UTs on the basis of their screening examination shall be eligible to appear in the National level examination to be conducted by the NCERT on the Second Sunday of May each year (unless otherwise notified).

<br><br>

If there is any change in pattern for Stage-II examination then the same will be communicated later on through
NCERT website <a href="www.ncert.nic.in">www.ncert.nic.in</a>. Candidates are advised to keep track of updates on NCERT website. There will
be no negative marking in Stage-II examination.
